-- 12. Выбрать из таблицы книги по программированию, не относящиеся к издательству «BHV»,
-- в названиях которых есть слово «Visual».
/*
 Предложение SELECT - выбираем данные из колонки Name
 Предложение FROM - данные из таблицы books
 Предложение WHERE - Значение колонки Press не равно (<>) и
                    значение колонки Name совпадает (LIKE) с паттерном "В начале/середине/конце встречается слово Visual"
 */
SELECT Name
FROM books
WHERE Press <> 'BHV'
    AND Name LIKE '%Visual%';

-- 13. Отобразить книги по программированию и Web-дизайну, которые относятся к издательствам «BHV» и «Бином».
/*
 Предложение SELECT - выбираем данные из колонки Name
 Предложение FROM - данные из таблицы books
 Предложение WHERE - Значение колонки Themes присутствует (IN) в списке ['Программирование', 'Web-дизайн']
                    и (AND) значение колонки Press присутствует (NOT IN) в списке ['BHV', 'Бином']
 */
SELECT Name
FROM books
WHERE Themes IN (N'Программирование', N'Web-дизайн')
    AND Press IN ('BHV', N'Бином');

-- 14. Выбрать книги, являющиеся справочниками или руководством.
/*
 Предложение SELECT - выбираем данные из колонки Name
 Предложение FROM - данные из таблицы books
 Предложение WHERE - Значение колонки Comment совпадает (LIKE) с паттерном "В начале/середине/конце встречается слово справочник"
                    или паттерном "В начале/середине/конце встречается слово руководство"
 */
SELECT Name
FROM books
WHERE Comment LIKE N'%справочник%'
    OR Comment LIKE N'%руководство%';

